public class Calc {
    static int add(int Num1, int Num2) {
        return Num1 + Num2;
    }

    static int subtract(int Num1, int Num2) {
        return Num1 - Num2;
    }

    static int multiply(int Num1, int Num2) {
        return Num1 * Num2;
    }

    static int divide(int Num1, int Num2) {
        return Num1 / Num2;
    }
}
